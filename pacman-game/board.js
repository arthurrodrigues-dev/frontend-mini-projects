function createSquare(x, y, width, height, color) {
    ctx.fillStyle = color;
    ctx.fillRect(x, y, width, height);
}

function drawBoard() {
    for (let i = 0; i < board.length; i++) {
        for (let j = 0; j < board[0].length; j++) {
            if (board[i][j] === 1) {
                createSquare(
                    j * blockSize,
                    i * blockSize,
                    blockSize,
                    blockSize,
                    wallColor
                );

            }
        }
    }
}

